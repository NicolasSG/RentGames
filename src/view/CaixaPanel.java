       private void atualizarSaldo() {
    lblSaldo.setText(String.format("R$ %.2f",
        util.DataStore.getInstance().getSaldoCaixa()).replace(".", ","));
}

    private void carregarTransacoes() {
    try {
        // 1. Pega o modelo da tabela
        javax.swing.table.DefaultTableModel mdl = 
            (javax.swing.table.DefaultTableModel) tblTransacoes.getModel();
        
        // 2. Limpa as linhas existentes
        mdl.setRowCount(0);
        
        // 3. Busca os dados do DataStore
        java.util.List<model.Transacao> list = util.DataStore.getInstance().getTransacoes();
        
        // 4. Adiciona cada transação como uma nova linha
        for (model.Transacao t : list) {
            Object[] linha = {
                t.getId(), 
                t.getDescricao(), 
                t.getTipo().toString(), 
                String.format("R$ %.2f", t.getValor()).replace(".", ","), 
                t.getData()
            };
            mdl.addRow(linha);
        }
        
        // 5. Força a tabela a se redesenhar (importante!)
        tblTransacoes.revalidate();
        tblTransacoes.repaint();
        
    } catch (Exception e) {
        System.err.println("Erro ao preencher a tabela: " + e.getMessage());
    }
}

