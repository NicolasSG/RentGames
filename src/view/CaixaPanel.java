    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        String desc = txtDescricao.getText().trim();
        String valS = txtValor.getText().trim();
        String data = txtData.getText().trim();

        if (desc.isEmpty() || valS.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Preencha descrição e valor.", "Aviso",
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            double valor = Double.parseDouble(valS.replace(",", "."));
            model.Transacao.Tipo tipo = "Entrada".equals(cmbTipo.getSelectedItem())
            ? model.Transacao.Tipo.ENTRADA : model.Transacao.Tipo.SAIDA;
            util.DataStore.getInstance().addTransacao(
                new model.Transacao(0, desc, tipo, valor, data));
            javax.swing.JOptionPane.showMessageDialog(this,
                "Transação registrada!", "Sucesso",
                javax.swing.JOptionPane.INFORMATION_MESSAGE);
            txtDescricao.setText("");
            txtValor.setText("");
            atualizarSaldo();
            carregarTransacoes();
        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Valor inválido.", "Erro",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

