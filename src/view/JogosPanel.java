    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        String nome = txtNome.getText().trim();
        String anoS = txtAno.getText().trim();
        String plat = txtPlataforma.getText().trim();

        if (nome.isEmpty() || anoS.isEmpty() || plat.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Preencha Nome, Ano e Plataforma.", "Aviso",
                javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            int    ano   = Integer.parseInt(anoS);
            double valor = txtValor.getText().trim().isEmpty() ? 0
            : Double.parseDouble(txtValor.getText().trim().replace(",", "."));
            int    est   = txtEstoque.getText().trim().isEmpty() ? 0
            : Integer.parseInt(txtEstoque.getText().trim());
            int    dias  = txtDias.getText().trim().isEmpty() ? 7
            : Integer.parseInt(txtDias.getText().trim());

            try {
            // ... (suas conversões de Integer.parseInt e Double.parseDouble)

            // Criando o objeto com os dados dos campos
            model.Jogo novoJogo = new model.Jogo(0, nome, ano, plat, valor, dias, est);

            // Agora o DAO aceita o objeto 'novoJogo' como único argumento
            jogoDAO.cadastrar(novoJogo); 

            javax.swing.JOptionPane.showMessageDialog(this, "Jogo cadastrado no MySQL!");

            // Limpa os campos e recarrega a tabela
            txtNome.setText(""); txtAno.setText(""); txtPlataforma.setText("");
            txtValor.setText(""); txtEstoque.setText(""); txtDias.setText("");
            carregarJogos();

            } catch (SQLException ex) {
                javax.swing.JOptionPane.showMessageDialog(this, "Erro no Banco: " + ex.getMessage());
            }   
            
        } catch (NumberFormatException ex) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Verifique os campos numéricos.", "Erro",
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

