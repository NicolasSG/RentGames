    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

                                                 
    int row = tblJogos.getSelectedRow();
    
    // Verifica se tem algo selecionado
    if (row < 0) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Selecione um jogo na tabela para excluir.", "Aviso",
            javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Pega o ID (está na coluna 0 da tabela)
    int id = (int) tblJogos.getValueAt(row, 0);
    String nome = (String) tblJogos.getValueAt(row, 1);

    int opt = javax.swing.JOptionPane.showConfirmDialog(this,
        "Tem certeza que deseja excluir o jogo \"" + nome + "\"?",
        "Confirmar Exclusão", javax.swing.JOptionPane.YES_NO_OPTION);

    if (opt == javax.swing.JOptionPane.YES_OPTION) {
        try {
            // Chama o DAO para deletar do banco
            jogoDAO.excluir(id);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Jogo removido com sucesso!");
            
            // Recarrega a tabela para sumir da tela
            carregarJogos();
            
        } catch (SQLException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Erro ao excluir no banco: " + ex.getMessage(), "Erro", 
                javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }

    }//GEN-LAST:event_btnExcluirActionPerformed

    
        private void onSelect() {
        int row = tblJogos.getSelectedRow();
        if (row >= 0) {
            java.util.List<model.Jogo> jogos = util.DataStore.getInstance().getJogos();
            if (row < jogos.size()) jogoSelecionado = jogos.get(row);
        }
    }

    public void carregarJogos() {
    javax.swing.table.DefaultTableModel mdl = (javax.swing.table.DefaultTableModel) tblJogos.getModel();
    mdl.setRowCount(0);
    
    try {
        // Agora busca do banco de dados!
        java.util.List<model.Jogo> lista = jogoDAO.listarTodos(); 
        
        for (model.Jogo j : lista) {
            mdl.addRow(new Object[]{
                j.getId(), 
                j.getNome(), 
                j.getPlataforma(),
                j.getAno(), 
                j.getValorAluguel(),
                j.getQuantidadeEstoque(), 
                j.getDiasAluguelPadrao()
            });
        }
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao carregar: " + e.getMessage());
    }
}   
 

